    public static JSONArray parseResponseArray(Response result) {
        //Try to get response body
        BufferedReader reader = null;
        StringBuilder sb = new StringBuilder();

        try {

            reader = new BufferedReader(new InputStreamReader(result.getBody().in()));

            String line;

            try {
                while ((line = reader.readLine()) != null) {
                    sb.append(line);
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        String myResult = sb.toString();
        JSONArray obj = null;
        try {
            obj = new JSONArray(myResult);
        } catch (JSONException e) {
            e.printStackTrace();
        }

        return obj;
    }

